#include "rush02lib.h"

int	ft_atoi(char	**argv)
{
	int i;
	int	n;

	i = 0;
	n = 0;
	while (argv[1][i] >= '0' && argv[1][i] <= '9')
	{
		n = n * 10 + (argv[1][i] - '0');
		i++;
	}
	return (n);
}
